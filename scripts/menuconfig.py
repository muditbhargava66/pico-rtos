    if get_config_value(kconf, 'ENABLE_ERROR_HISTORY') == 'y':
        header_content += f"#define PICO_RTOS_ERROR_HISTORY_SIZE {get_config_value(kconf, 'ERROR_HISTORY_SIZE', '10')}\n"
    header_content += "\n"
    
    # Debug logging
    header_content += "/* Debug Logging Configuration */\n"
